package org.ccar.data;

/**
 * 表示景点。
 * 
 * @author FG
 * 
 */
public class ScenicSpot {

	/**
	 * 景点的 ID。
	 */
	public int ID;

	/**
	 * 景点的名称。
	 */
	public String Name;

	/**
	 * 景点的描述。
	 */
	public String Description;

	/**
	 * 景点的 X 坐标。
	 */
	public double X;

	/**
	 * 景点的 Y 坐标。
	 */
	public double Y;

}
